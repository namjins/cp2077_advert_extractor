def main() -> None:
    cfg = load_config(Path("config.toml"))
    rows = read_manifest(cfg.discovery.approved_manifest)

    matched = 0
    for row in rows:
        if row.editable_source_path:
            fname = PurePosixPath(row.editable_source_path).name
            if fname in SKIP_FILES:
                row.status = "skipped"
                row.notes = "not visual ad imagery"
                matched += 1

    write_manifest(cfg.discovery.approved_manifest, rows)

    total = len(rows)
    remaining = sum(1 for r in rows if r.status in ("approved", "ready"))
    print(f"Marked {matched} rows as skipped out of {total} total.")
    print(f"Remaining approved/ready: {remaining}")


if __name__ == "__main__":
    main()
